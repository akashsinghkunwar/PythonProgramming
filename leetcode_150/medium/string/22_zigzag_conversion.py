class Solution(object):
    def convert(self, s, numRows):
        """
        :type s: str
        :type numRows: int
        :rtype: str
        """
        if numRows == 1 or numRows >= len(s):
            return s

        rows = [""] * min(numRows, len(s))
        currRow = 0
        down = False

        for c in s:
            rows[currRow] += c
            if currRow == 0 or currRow == numRows - 1:
                down = not down
            currRow += 1 if down else -1
        return "".join(rows)


